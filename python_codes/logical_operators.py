#logical operators

#it is used to combine boolean values or comparison operators
value1 = True
value2 = True
value3 = False
value4 = False

#true or true
print(value1 or value2)

#true or false
print(value1 or value3)

#false or false
print(value3 or value4)

#true and true
print(value1 and value2)

#true and false
print(value2 and value3)

#false and false
print(value3 and value4)

#not true
print(not value1)

#not false
print(not value3)