#comparison operators
#comparison operators returns binary, either true or false
value1 = 10
value2 = 5

#equal, ==
print(value1 == value2)

#not equal, !=
print(value1 != value2)

#greater than, >
print(value1 > value2)

#less than, <
print(value1 < value2)

#greater or equal to, >=
print(value1 >= value2)

#less than or equal to, <=
print(value1 <= value2)