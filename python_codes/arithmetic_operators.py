#arithmetic operators in python
value1 = 15
value2 = 5
#addition
print(value1+value2)

#substraction
print(value1-value2)

#multiplication
print(value1*value2)

#division
print(value1/value2)

#modulus
#modulus returns a remainder
print(value1%value2)

#exponent **
print(value1**value2)

#floor division
#returns the result of a division without decimal places
print(value1//value2)