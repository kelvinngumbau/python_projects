#assignment operators
#assigning a value to a variable
value1 = 10
print(value1)

#add a value to a varaible
value1 +=5
print(value1)

#substract a value from a variable
value1 -= 5
print(value1)

#multiply the value with a variable
value1 *= 10
print(value1)

#divide the value with a variable
value1 /= 10
print(value1)

#assign the remainder of the value
value1 %= 6
print(value1)

#assign the result of an exponent
value1 **= 4
print(value1)

#assign the result of the floor divison
value1 //= 4
print(value1) 

